/*Извикващата функция има право да пренебрегне т.е. да не използва
върнатата стойност както е извикването на add(a,b); боядисан в червено,
защото върнатата стойност не се присвоява на променлива. */
#include <stdio.h>


int add(int a, int b){
return (a + b);
}

int main(void){
int a =1, b = 2, res = 0;
res = add(a, b);
printf("%d\n", res);
add(a,b); //игнорираме резултата от функцията
return 0;
}